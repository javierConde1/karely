    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        Connection con = null;
        String sql = "delete from producto where codigo = ?";
        try {
            con = getConnection();
            ps = con.prepareStatement(sql);
            ps.setInt(1, Integer.parseInt(jTextField1.getText()));

            int rs2 = ps.executeUpdate();

            if (rs2 > 0) {
                JOptionPane.showMessageDialog(null, "REGISTRO BORRADO");
                limpiarCajas();
            } else {
                JOptionPane.showMessageDialog(null, "CODIGO INCORRECTO");
                limpiarCajas();
            }

        } catch (Exception e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Connection con = null;
        String sql = "select * from producto where codigo = ?";
        try {
            con = getConnection();
            ps = con.prepareStatement(sql);
            ps.setString(1, jTextField1.getText());

            rs = ps.executeQuery();

            if (rs.next()) {
                jTextField1.setText(rs.getString("codigo"));
                jTextField2.setText(rs.getString("nombre"));
                jTextField3.setText(rs.getString("precio"));
                jTextField5.setText(rs.getString("precio_prov"));
                jTextField4.setText(rs.getString("stock"));
            } else {
                JOptionPane.showMessageDialog(null, "CODIGO INCORRECTO");
            }

        } catch (Exception e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

